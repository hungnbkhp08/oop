    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        tongluong = 0;
        dsnv.clear();
        try {
            File file = new File("nhanvien.dat");
            Scanner sc = new Scanner(file);
            while (sc.hasNextLine()) {
                String[] data = sc.nextLine().split("\\-");
                NhanVien nhavie = new NhanVien();
                nhavie.setMsv(data[0]);
                nhavie.setHoten(data[1]);
                nhavie.setSdt(Long.valueOf(data[2]));
                nhavie.setHsl(Integer.parseInt(data[3]));
                nhavie.setLuong(Integer.parseInt(data[4]));
                dsnv.add(nhavie);
            }
            for (NhanVien nhanvien : dsnv) {
                tongluong = tongluong + nhanvien.Luongthuclinh();
            }
        } catch (FileNotFoundException ex) {
        }

        txtTongluong.setText(String.valueOf(tongluong));
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        dsnv.clear();
        tableModel.setRowCount(0);
        txtDSNV.setText("");
        try {
            File file = new File("nhanvien.dat");
            Scanner sc = new Scanner(file);
            while (sc.hasNextLine()) {
                String[] data = sc.nextLine().split("\\-");
                NhanVien nhavie = new NhanVien();
                nhavie.setMsv(data[0]);
                nhavie.setHoten(data[1]);
                nhavie.setSdt(Long.valueOf(data[2]));
                nhavie.setHsl(Integer.parseInt(data[3]));
                nhavie.setLuong(Integer.parseInt(data[4]));
                dsnv.add(nhavie);
            }
            StringBuilder danhSach = new StringBuilder();
            for (NhanVien nhavie : dsnv) {
                Object[] row = new Object[]{nhavie.getMsv(), nhavie.getHoten(), nhavie.getSdt(), nhavie.getHsl(), nhavie.getLuong()
                };
                tableModel.addRow(row);
                tableModel.fireTableDataChanged();
                danhSach.append("- ").append(nhavie.getMsv()).append(", ")
                        .append(nhavie.getHoten()).append(", ")
                        .append(nhavie.getSdt()).append(", ")
                        .append(nhavie.getHsl()).append(", ")
                        .append(nhavie.getLuong()).append( System.lineSeparator());
            }
            txtDSNV.setText(danhSach.toString());
            JOptionPane.showMessageDialog(rootPane, "Đọc file thành công");
        } catch (FileNotFoundException ex) {
        }

    }//GEN-LAST:event_jButton3ActionPerformed

