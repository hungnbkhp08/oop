    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        NhanVien nhv = new NhanVien();
        nhv.setMsv(txtMnv.getText());
        nhv.setHoten(txtHoten.getText());
        nhv.setSdt(Long.valueOf(txtDienthoai.getText()));
        nhv.setHsl(Integer.parseInt(txtHesoluong.getText()));
        nhv.setLuong(Integer.parseInt(txtLuong.getText()));
        dsnv.add(nhv);
        try {
            File file = new File("nhanvien.dat");
            PrintWriter printWriter = new PrintWriter(file);
            for (NhanVien nv : dsnv) {
                printWriter.printf("%s-%s-%d-%d-%d\n", nv.getMsv(), nv.getHoten(), nv.getSdt(), nv.getHsl(), nv.getLuong());
            }
            printWriter.close();
            JOptionPane.showMessageDialog(rootPane, "Ghi file thành công");
        } catch (FileNotFoundException ex) {

        }
    }//GEN-LAST:event_jButton2ActionPerformed

